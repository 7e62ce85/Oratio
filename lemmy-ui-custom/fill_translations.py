count = 0
for lang_code, translations in ALL_TRANSLATIONS.items():
    if lang_code.endswith("_missing"):
        # These are extra keys for existing languages (de, fr, es, ru)
        real_lang = lang_code.replace("_missing", "")
        if real_lang not in data:
            continue
        for key, val in translations.items():
            if key not in data[real_lang]:
                data[real_lang][key] = val
                count += 1
    else:
        if lang_code not in data:
            data[lang_code] = {}
        for key, val in translations.items():
            if key not in data[lang_code]:
                data[lang_code][key] = val
                count += 1

